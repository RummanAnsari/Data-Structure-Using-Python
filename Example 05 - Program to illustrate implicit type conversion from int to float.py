'''
Example 5: Program to illustrate implicit type conversion from int to float.
'''

# Define an integer value
num_int = 10

# Convert the integer value to float
num_float = num_int + 12.0

# Print both values with their data types
print("The integer value is:", num_int, "and its data type is:", type(num_int))
print("The float value is:", num_float, "and its data type is:", type(num_float))
